package 윤영욱교수님;

public class Practice_2_02 {

	public static void main(String[] args) {
		/*
		Find the Area of a Circle Write a program to find and print 
		the area of a circle. 
		Algorithm for calculating the area of a circle 
		(1) Read the radius of the circle 
		(2) Find the area of the circle using the formula 
				area = radius * radius * pi 
		(3) Print the result
		*/
		final double PI = 3.14;
		int r = 3;
		double area;
		// Definition
		area = r * r * PI;
		System.out.println(area);
	}

}
