package 윤영욱교수님;

public class Practice_2_03 {

	public static void main(String[] args) {
		// Let's check out the range of numeric data 
		//types that Java provides.
		
		// Range of byte Type
		System.out.println("Ranges of byte type: ");
		System.out.println("Max: " + Byte.MAX_VALUE);
		System.out.println("Min: " + Byte.MIN_VALUE);
		
		// Ranges of short Type
		System.out.println("Ranges of short type: ");
		System.out.println("Max:" + Short.MAX_VALUE);
		System.out.println("Min:" + Short.MIN_VALUE);
		
		// Ranges of int Type
		System.out.println("Ranges of int type: ");
		System.out.println("Max: " + Integer.MAX_VALUE);
		System.out.println("Min: " + Integer.MIN_VALUE);
		
		// Ranges of long Type
		System.out.println("Ranges of long type: ");
		System.out.println("Max: " + Long.MAX_VALUE);
		System.out.println("Min: " + Long.MIN_VALUE);

		// Ranges of float Type
		System.out.println("Ranges of float type: ");
		System.out.println("Max: " + Float.MAX_VALUE);
		System.out.println("Min: " + Float.MIN_VALUE);
		
		// Ranges of double Type
		System.out.println("Ranges of double type: ");
		System.out.println("Max: " + Double.MAX_VALUE);
		System.out.println("Min: " + Double.MIN_VALUE);
		
	}

}
